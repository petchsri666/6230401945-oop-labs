weekday = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"]
print("Weekday are", weekday)
weekend = ["Saturday", "Sunday"]
print("Weeken are", weekend)
days = weekday + weekend
print("Days are", days)
sorted_days = sorted(days)
print("Sorted days are", sorted_days)
reversed_days = list(reversed(days))
print("Reversed days are", reversed_days)
print("The last two days of list days are", days[5:7])